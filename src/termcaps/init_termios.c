#include "sh21.h"

void				init_termios(void)
{
	char			*name;
	struct termios	term;

	if ((name = getenv("TERM")) == NULL)
		return ;
	if (tgetent(NULL, name) <= 0)
		return ;
	if (tcgetattr(0, &term) == -1)
		return ;
	term.c_lflag &= ~(ICANON);
	term.c_lflag &= ~(ECHO);
	term.c_cc[VMIN] = 1;
	term.c_cc[VTIME] = 0;
	if (tcsetattr(0, TCSADRAIN, &term) == -1)
		return ;
}
